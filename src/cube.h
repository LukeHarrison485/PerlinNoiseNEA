unsigned int textures[3];

class cube {
public:
    glm::vec3 position;
    TextureID textID;
    std::vector<cube*>* cubes;
    unsigned int* VAO;
    Shader* cubeShader;
    cube(glm::vec3 pos, TextureID textureID, std::vector<cube*>& arr, unsigned int* vao, Shader* shader) {
        position = pos;
        textID = textureID;
        arr.push_back(this);
        if(*vao == 0) {
            unsigned int VBO, cubeVAO;
            glGenVertexArrays(1, &cubeVAO);
            glGenBuffers(1, &VBO);

            glBindBuffer(GL_ARRAY_BUFFER, VBO);
            glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

            glBindVertexArray(cubeVAO);

            // position attribute
            glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
            glEnableVertexAttribArray(0);
            // normal attribute
            glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));
            glEnableVertexAttribArray(1);
            glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));
            glEnableVertexAttribArray(2);
            VAO = vao;
            cubeShader = shader;
        }
        VAO = vao;
    }   
    void drawCube(Shader* sahader) {
        glm::mat4 model = glm::mat4(1.0f);
        glBindTexture(GL_TEXTURE_2D, textures[textID]);
        model = glm::translate(model, position);
        sahader->setMat4("model", model);
        glDrawArrays(GL_TRIANGLES, 0, 36);
    }
};

std::vector<cube*> cubes;

void drawWorld(unsigned int VAO, Shader* shader) {
    glBindVertexArray(VAO);
    for(cube* c : cubes) {
        c->drawCube(shader);
    }
    glBindVertexArray(0);
}

